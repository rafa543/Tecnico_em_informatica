"""
Operadores Aritimeticos
Operador    Ação 

+           Adição
-           Subtração
*           Multiplicação
/           Divisão
//          Divisao inteiro
**          Potenciação
%           REsto de divisao
()          Procedencia


"""

print('Adição: 2 + 5 = {}'.format(2 + 5))
print('Subtração: 2 - 5 = {}'.format(2 - 5))
print('Multiplicação: 2 * 5 = {}'.format(2 * 5))
print('Divisão: 2 / 5 = {}'.format(2 / 5))
print('Divisao inteiro: 22 // 5 = {}'.format(22 // 5))
print('Potenciacao: 2 ** 8 = {}'.format(2 ** 8))
print('Resto: 2 % 5 = {}'.format(2 % 5))
print('Sem procedencia: 5 + 2 * 10 = {}'.format(5 + 2 * 10))
print('Com procedencia:(5 + 2) * 10 = {}'.format((5 + 2) * 10))
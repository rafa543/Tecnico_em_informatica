numero = int(input("Digite um valor inteiro "))

print("Seu numero é:", numero)
print(f"O antecessor deste número é: {numero - 1}")
print(f"O sucessor deste numero é: {numero + 1}")

print(f"{numero} * 10 = {numero * 10}")
print(f"{numero} ** 2 = {numero ** 2}")
print(f"{numero} / 2 = {numero / 2}")
print(f"{numero} % 2 = {numero % 2}")


